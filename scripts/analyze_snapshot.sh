  bandwidth_lines="$(grep -E 'pci 0000:0[0-9]:00\.0: .*available PCIe bandwidth|pci 0000:03:00\.0: .*available PCIe bandwidth' "${kernel_file}" 2>/dev/null || true)"

  {
    echo "## BIOS / PCIe Topology"
