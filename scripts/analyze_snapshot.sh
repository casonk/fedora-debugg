#!/usr/bin/env bash
set -u -o pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ROOT_DIR="$(cd "${SCRIPT_DIR}/.." && pwd)"
ARTIFACTS_DIR="${ROOT_DIR}/artifacts"

usage() {
  cat <<'EOF'
Usage: ./scripts/analyze_snapshot.sh [snapshot_dir]

Analyze a snapshot generated by collect_snapshot.sh.
If snapshot_dir is omitted, the latest snapshot is used.
EOF
}

if [ "${1:-}" = "--help" ] || [ "${1:-}" = "-h" ]; then
  usage
  exit 0
fi

if [ $# -gt 1 ]; then
  usage >&2
  exit 1
fi

if [ $# -eq 1 ]; then
  SNAPSHOT_DIR="$1"
else
  if [ -L "${ARTIFACTS_DIR}/latest" ] || [ -d "${ARTIFACTS_DIR}/latest" ]; then
    SNAPSHOT_DIR="$(cd "${ARTIFACTS_DIR}" && pwd)/$(readlink "${ARTIFACTS_DIR}/latest")"
  else
    SNAPSHOT_DIR="$(ls -1dt "${ARTIFACTS_DIR}"/snapshot-* 2>/dev/null | head -n 1 || true)"
  fi
fi

if [ -z "${SNAPSHOT_DIR}" ] || [ ! -d "${SNAPSHOT_DIR}" ]; then
  echo "No snapshot found to analyze." >&2
  exit 1
fi

COMMANDS_DIR="${SNAPSHOT_DIR}/commands"
SUMMARY_FILE="${SNAPSHOT_DIR}/analysis-summary.md"

if [ ! -d "${COMMANDS_DIR}" ]; then
  echo "Invalid snapshot: missing commands directory: ${COMMANDS_DIR}" >&2
  exit 1
fi

mapfile -t LOG_FILES < <(find "${COMMANDS_DIR}" -maxdepth 1 -type f | sort)

if [ "${#LOG_FILES[@]}" -eq 0 ]; then
  echo "No command output files found in ${COMMANDS_DIR}" >&2
  exit 1
fi

render_header() {
  {
    echo "# Crash Analysis Summary"
    echo
    echo "- Snapshot: ${SNAPSHOT_DIR}"
    echo "- Generated: $(date --iso-8601=seconds)"
    echo
  } >"${SUMMARY_FILE}"
}

render_collection_health() {
  local failures
  failures="$(grep -H '^# Exit status: [1-9]' "${COMMANDS_DIR}"/*.txt 2>/dev/null || true)"

  {
    echo "## Collection Health"
    if [ -n "${failures}" ]; then
      echo
      echo "Some collection commands returned non-zero exit statuses."
      echo "This is usually due to permissions or missing commands."
      echo
      printf '%s\n' '```text'
      echo "${failures}" | sed "s|${COMMANDS_DIR}/||g"
      printf '%s\n' '```'
    else
      echo
      echo "All collection commands exited with status 0."
    fi
    echo
  } >>"${SUMMARY_FILE}"
}

collect_matches() {
  local regex="$1"
  shift || true

  if [ $# -gt 0 ]; then
    grep -HnEi "${regex}" "$@" 2>/dev/null || true
  else
    grep -HnEi "${regex}" "${LOG_FILES[@]}" 2>/dev/null || true
  fi
}

count_matches() {
  local regex="$1"
  shift || true
  local matches

  matches="$(collect_matches "${regex}" "$@")"
  printf '%s\n' "${matches}" | sed '/^$/d' | wc -l | tr -d ' '
}

payload_lines() {
  local file="$1"
  [ -f "${file}" ] || return 0
  grep -vE '^(# |$|SKIPPED:|MISSING:)' "${file}" 2>/dev/null || true
}

count_payload_lines() {
  local file="$1"
  local exclude_regex="${2:-}"
  local lines

  lines="$(payload_lines "${file}")"
  if [ -n "${exclude_regex}" ]; then
    lines="$(printf '%s\n' "${lines}" | grep -Eiv "${exclude_regex}" 2>/dev/null || true)"
  fi
  printf '%s\n' "${lines}" | sed '/^$/d' | wc -l | tr -d ' '
}

append_pattern_section() {
  local label="$1"
  local regex="$2"
  local matches
  local count

  matches="$(collect_matches "${regex}")"
  count="$(count_matches "${regex}")"

  {
    echo "## ${label}"
    echo
    echo "- Matches: ${count}"
    if [ "${count}" -gt 0 ]; then
      echo
      printf '%s\n' '```text'
      printf '%s\n' "${matches}" | sed "s|${COMMANDS_DIR}/||g" | sed -n '1,25p'
      printf '%s\n' '```'
    fi
    echo
  } >>"${SUMMARY_FILE}"
}

append_pattern_section_from_files() {
  local label="$1"
  local regex="$2"
  shift 2
  local matches
  local count

  matches="$(collect_matches "${regex}" "$@")"
  count="$(printf '%s\n' "${matches}" | sed '/^$/d' | wc -l | tr -d ' ')"

  {
    echo "## ${label}"
    echo
    echo "- Matches: ${count}"
    if [ "${count}" -gt 0 ]; then
      echo
      printf '%s\n' '```text'
      printf '%s\n' "${matches}" | sed "s|${COMMANDS_DIR}/||g" | sed -n '1,25p'
      printf '%s\n' '```'
    fi
    echo
  } >>"${SUMMARY_FILE}"
}

read_kv_value() {
  local file="$1"
  local key="$2"
  if [ ! -f "${file}" ]; then
    return 0
  fi
  awk -v key="${key}" 'index($0, key "=") == 1 {print substr($0, length(key) + 2); exit}' "${file}" 2>/dev/null
}

extract_findmnt_options() {
  local target="$1"
  local file="${COMMANDS_DIR}/findmnt.txt"
  if [ ! -f "${file}" ]; then
    return 0
  fi
  awk -v target="${target}" '
    {
      mount_target = $1
      sub(/^[^/]+/, "", mount_target)
      if (mount_target == target) {
        print $NF
        exit
      }
    }
  ' "${file}" 2>/dev/null
}

mount_mode_from_options() {
  local options="$1"
  case ",${options}," in
    *,ro,*) echo "ro" ;;
    *,rw,*) echo "rw" ;;
    *) echo "unknown" ;;
  esac
}

boot_label_from_index() {
  case "$1" in
    0) echo "current" ;;
    -1) echo "prev-1" ;;
    -2) echo "prev-2" ;;
    *) echo "$1" ;;
  esac
}

boot_warn_file_for_index() {
  case "$1" in
    0) printf '%s\n' "${COMMANDS_DIR}/journal-current-warn.txt" ;;
    -1) printf '%s\n' "${COMMANDS_DIR}/journal-prev-warn.txt" ;;
    -2) printf '%s\n' "${COMMANDS_DIR}/journal-prev2-warn.txt" ;;
    *) return 1 ;;
  esac
}

boot_window_from_list() {
  local idx="$1"
  local file="${COMMANDS_DIR}/journal-list-boots.txt"
  if [ ! -f "${file}" ]; then
    return 0
  fi

  awk -v idx="${idx}" '
    $1 == idx {
      sub(/^[[:space:]]+/, "", $0)
      print
      exit
    }
  ' "${file}" 2>/dev/null
}

count_session_entries() {
  local file="$1"
  local section_header="$2"

  if [ ! -f "${file}" ]; then
    echo "0"
    return 0
  fi

  awk -v section_header="${section_header}" '
    $0 == section_header {
      in_section = 1
      next
    }
    /^# / && in_section {
      exit
    }
    in_section && NF && $0 !~ /^MISSING:/ {
      count++
    }
    END {
      print count + 0
    }
  ' "${file}" 2>/dev/null
}

rpm_query_pkg_state() {
  local file="$1"
  local pkg="$2"

  if [ ! -f "${file}" ]; then
    echo "unknown"
    return 0
  fi

  if grep -Eq "^${pkg}-[0-9]" "${file}" 2>/dev/null; then
    echo "installed"
  elif grep -Eq "^package ${pkg} is not installed" "${file}" 2>/dev/null; then
    echo "missing"
  else
    echo "unknown"
  fi
}

collect_nvidia_invalid_mmap_bursts() {
  local file

  for file in "$@"; do
    [ -f "${file}" ] || continue

    awk -v prefix="${file}" '
      /NVRM: VM: invalid mmap/ {
        ts = $1 " " $2 " " $3
        if (count > 0 && ts == last_ts) {
          count++
          next
        }
        if (count > 0) {
          printf "%s:%d:%s: NVRM: VM: invalid mmap (burst x%d)\n", prefix, first_line, last_ts, count
        }
        last_ts = ts
        first_line = FNR
        count = 1
        next
      }
      count > 0 {
        printf "%s:%d:%s: NVRM: VM: invalid mmap (burst x%d)\n", prefix, first_line, last_ts, count
        count = 0
        last_ts = ""
      }
      END {
        if (count > 0) {
          printf "%s:%d:%s: NVRM: VM: invalid mmap (burst x%d)\n", prefix, first_line, last_ts, count
        }
      }
    ' "${file}" 2>/dev/null
  done
}

count_nvidia_invalid_mmap_bursts() {
  local bursts

  bursts="$(collect_nvidia_invalid_mmap_bursts "$@")"
  printf '%s\n' "${bursts}" | sed '/^$/d' | wc -l | tr -d ' '
}

collect_gdm_idle_suspend_windows() {
  local file="$1"
  local line
  local timestamp
  local marker_timestamp=""
  local marker_epoch=""
  local marker_label=""
  local request_epoch
  local elapsed
  local manual_request=0

  [ -f "${file}" ] || return 0

  while IFS= read -r line; do
    timestamp="${line%% *}"

    case "${line}" in
      "-- Boot "*)
        marker_timestamp=""
        marker_epoch=""
        marker_label=""
        manual_request=0
        ;;
      *"New session "*"gdm-greeter"*)
        marker_timestamp="${timestamp}"
        marker_epoch="$(date -d "${timestamp}" +%s 2>/dev/null || true)"
        marker_label="GDM greeter start"
        manual_request=0
        ;;
      *"PM: suspend exit"*|*"System returned from sleep operation"*)
        marker_timestamp="${timestamp}"
        marker_epoch="$(date -d "${timestamp}" +%s 2>/dev/null || true)"
        marker_label="resume"
        manual_request=0
        ;;
      *"Power key pressed"*|*"Suspend key pressed"*|*"Lid closed"*)
        manual_request=1
        ;;
      *"The system will suspend now!"*)
        request_epoch="$(date -d "${timestamp}" +%s 2>/dev/null || true)"
        if [ -n "${marker_epoch}" ] && [ -n "${request_epoch}" ] && [ "${manual_request}" -eq 0 ]; then
          elapsed=$((request_epoch - marker_epoch))
          if [ "${elapsed}" -ge 840 ] && [ "${elapsed}" -le 1260 ]; then
            printf '%s -> %s (%ss): %s -> suspend request\n' \
              "${marker_timestamp}" "${timestamp}" "${elapsed}" "${marker_label}"
          fi
        fi
        marker_timestamp=""
        marker_epoch=""
        marker_label=""
        manual_request=0
        ;;
    esac
  done <"${file}"
}

render_suspend_triage() {
  local events_file="${COMMANDS_DIR}/journal-suspend-events.txt"
  local overrides_file="${COMMANDS_DIR}/gdm-power-overrides.txt"
  local profile_file="${COMMANDS_DIR}/gdm-dconf-profile.txt"
  local suspend_count
  local resume_count
  local input_count
  local gdm_start_count
  local idle_windows
  local idle_window_count
  local gdm_override_state
  local gdm_profile_state

  suspend_count="$(count_matches "the system will suspend now" "${events_file}")"
  resume_count="$(count_matches "pm: suspend exit|system returned from sleep operation" "${events_file}")"
  input_count="$(count_matches "power key pressed|suspend key pressed|lid closed" "${events_file}")"
  gdm_start_count="$(count_matches "new session .*gdm-greeter" "${events_file}")"
  idle_windows="$(collect_gdm_idle_suspend_windows "${events_file}")"
  idle_window_count="$(printf '%s\n' "${idle_windows}" | sed '/^$/d' | wc -l | tr -d ' ')"

  if [ -f "${overrides_file}" ] \
    && grep -Eq "^sleep-inactive-ac-timeout=0$" "${overrides_file}" 2>/dev/null \
    && grep -Eq "^sleep-inactive-ac-type='nothing'$" "${overrides_file}" 2>/dev/null \
    && grep -Eq "^sleep-inactive-battery-timeout=0$" "${overrides_file}" 2>/dev/null \
    && grep -Eq "^sleep-inactive-battery-type='nothing'$" "${overrides_file}" 2>/dev/null; then
    gdm_override_state="complete"
  elif [ -f "${overrides_file}" ] && grep -Eq "sleep-inactive-(ac|battery)-(timeout|type)" "${overrides_file}" 2>/dev/null; then
    gdm_override_state="partial"
  elif [ -f "${overrides_file}" ]; then
    gdm_override_state="not present"
  else
    gdm_override_state="not captured"
  fi

  if [ -f "${profile_file}" ] && grep -Eq '^system-db:gdm$' "${profile_file}" 2>/dev/null; then
    gdm_profile_state="active"
  elif [ -f "${profile_file}" ]; then
    gdm_profile_state="not active"
  else
    gdm_profile_state="not captured"
  fi

  {
    echo "## Suspend Triage"
    echo
    echo "- Suspend requests captured: ${suspend_count}"
    echo "- Resume events captured: ${resume_count}"
    echo "- Power/suspend-key or lid events captured: ${input_count}"
    echo "- GDM greeter starts captured: ${gdm_start_count}"
    echo "- GDM/default-idle timing matches: ${idle_window_count}"
    echo "- GDM system dconf profile: ${gdm_profile_state}"
    echo "- GDM no-auto-suspend override: ${gdm_override_state}"
    echo

    if [ "${idle_window_count}" -gt 0 ]; then
      echo "Likely cluster:"
      if [ "${idle_window_count}" -ge 2 ]; then
        echo "- Repeated suspend requests occur about 15 minutes after GDM starts or the machine resumes, strongly indicating the GDM greeter's default idle-suspend policy."
      else
        echo "- A suspend request occurred about 15 minutes after GDM started or the machine resumed, consistent with the GDM greeter's default idle-suspend policy."
      fi
      echo "- Terminal, SSH, and TTY activity does not reset the graphical GDM greeter's idle timer."
      if [ "${gdm_override_state}" != "complete" ]; then
        echo "- Install the repo's GDM-only override with: sudo ./scripts/install_gdm_no_auto_suspend.sh"
      fi
      if [ "${input_count}" -gt 0 ]; then
        echo "- Physical key/lid events also exist in the captured history, but the timing matches below exclude those events."
      fi
      echo
      printf '%s\n' '```text'
      printf '%s\n' "${idle_windows}" | sed -n '1,25p'
      printf '%s\n' '```'
      echo
    elif [ "${suspend_count}" -gt 0 ]; then
      echo "Suspend requests were captured, but they do not yet form a repeated GDM/default-idle timing pattern."
      echo
    fi
  } >>"${SUMMARY_FILE}"
}

render_resume_context() {
  local reboot_matches
  local reboot_count
  local session_regex
  local session_matches
  local session_count

  reboot_matches=""
  if [ -f "${COMMANDS_DIR}/last-reboots.txt" ]; then
    reboot_matches="$(
      awk 'NR <= 10 && /crash|gone - no logout/ {print "last-reboots.txt:" NR ":" $0}' \
        "${COMMANDS_DIR}/last-reboots.txt" 2>/dev/null || true
    )"
  fi
  reboot_count="$(printf '%s\n' "${reboot_matches}" | sed '/^$/d' | wc -l | tr -d ' ')"

  session_regex="process [0-9]+ \\(gnome-shell\\) of user [0-9]+ dumped core|org\\.gnome\\.Shell@wayland\\.service: (main process exited|failed with result)|the wayland connection broke|user@[0-9]+\\.service: main process exited, code=dumped|process [0-9]+ \\(gnome-session-i\\) of user [0-9]+ dumped core"
  session_matches="$(collect_matches "${session_regex}" "${COMMANDS_DIR}/journal-current-warn.txt" "${COMMANDS_DIR}/journal-prev-warn.txt")"
  session_count="$(printf '%s\n' "${session_matches}" | sed '/^$/d' | wc -l | tr -d ' ')"

  {
    echo "## Resume Context"
    echo
    echo "- Recent reboot/crash markers: ${reboot_count}"
    echo "- Compositor/session crash markers near the latest reboot: ${session_count}"
    echo

    if [ "${reboot_count}" -gt 0 ] || [ "${session_count}" -gt 0 ]; then
      echo "Likely cluster:"
      if [ "${reboot_count}" -gt 0 ]; then
        echo "- The latest boot followed an unclean shutdown/crash rather than a clean reboot."
      fi
      if [ "${session_count}" -gt 0 ]; then
        echo "- The previous user session appears to have died with a Wayland/GNOME Shell compositor crash."
      fi
      echo
      printf '%s\n' '```text'
      if [ "${reboot_count}" -gt 0 ]; then
        printf '%s\n' "${reboot_matches}"
      fi
      if [ "${session_count}" -gt 0 ]; then
        printf '%s\n' "${session_matches}" | sed "s|${COMMANDS_DIR}/||g" | sed -n '1,10p'
      fi
      printf '%s\n' '```'
      echo
    fi
  } >>"${SUMMARY_FILE}"
}

render_recent_reboot_triage() {
  local boot_idx
  local boot_file
  local boot_label
  local boot_window
  local codium_regex
  local session_regex
  local wayland_regex
  local nvidia_fault_regex
  local nvidia_mmap_regex
  local compiler_regex
  local memory_fault_regex
  local storage_regex
  local combined_regex
  local codium_count
  local session_count
  local wayland_count
  local nvidia_fault_count
  local nvidia_mmap_burst_count
  local compiler_fault_count
  local storage_count
  local boot_matches
  local analyzed_boots=0
  local summary_rows=""
  local notable_matches=""

  codium_regex="traps: codium|codium\\[[0-9]+\\].*(trap int3|invalid opcode|general protection fault)|process [0-9]+ \\(codium\\) of user [0-9]+ dumped core|/usr/share/codium/codium.*sig(segv|trap|ill)"
  session_regex="process [0-9]+ \\(gnome-shell\\) of user [0-9]+ dumped core|org\\.gnome\\.Shell@wayland\\.service: (main process exited|failed with result)|the wayland connection broke|user@[0-9]+\\.service: main process exited, code=dumped|process [0-9]+ \\(gnome-session-i\\) of user [0-9]+ dumped core"
  wayland_regex="connection to xwayland lost|invalid sequence for vsync frame info|the wayland connection broke|cannot open display: :0|qt\\.qpa\\.wayland"
  nvidia_fault_regex="nvrm: xid|nvidia-drm.*error|gpu has fallen off|nvidia-smi has failed|failed to initialize nvml"
  nvidia_mmap_regex="nvrm: vm: invalid mmap"
  compiler_regex="internal compiler error|/usr/libexec/gcc/.*/cc1|comm: cc1|terminated abnormally without generating a coredump"
  memory_fault_regex="corrupted page table|bad pagetable|oops:|exited with irqs disabled"
  storage_regex="btrfs|ext4-fs error|xfs|nvme.*error|read-only file system|remount-ro"
  combined_regex="${codium_regex}|${session_regex}|${wayland_regex}|${nvidia_fault_regex}|${nvidia_mmap_regex}|${compiler_regex}|${memory_fault_regex}|${storage_regex}"

  for boot_idx in 0 -1 -2; do
    boot_file="$(boot_warn_file_for_index "${boot_idx}" || true)"
    boot_label="$(boot_label_from_index "${boot_idx}")"
    boot_window="$(boot_window_from_list "${boot_idx}")"

    if [ -z "${boot_window}" ] && { [ -z "${boot_file}" ] || [ ! -f "${boot_file}" ]; }; then
      continue
    fi

    analyzed_boots=$((analyzed_boots + 1))
    codium_count="$(count_matches "${codium_regex}" "${boot_file}")"
    session_count="$(count_matches "${session_regex}" "${boot_file}")"
    wayland_count="$(count_matches "${wayland_regex}" "${boot_file}")"
    nvidia_fault_count="$(count_matches "${nvidia_fault_regex}" "${boot_file}")"
    nvidia_mmap_burst_count="$(count_nvidia_invalid_mmap_bursts "${boot_file}")"
    compiler_fault_count="$(count_matches "${compiler_regex}" "${boot_file}")"
    memory_fault_count="$(count_matches "${memory_fault_regex}" "${boot_file}")"
    storage_count="$(count_matches "${storage_regex}" "${boot_file}")"

    summary_rows="${summary_rows}boot ${boot_label} (${boot_idx}) | ${boot_window:-boot metadata unavailable}\n"
    summary_rows="${summary_rows}  indicators: codium=${codium_count} compositor=${session_count} wayland=${wayland_count} nvidia_faults=${nvidia_fault_count} nvidia_vm_bursts=${nvidia_mmap_burst_count} compiler_faults=${compiler_fault_count} mem_faults=${memory_fault_count} storage=${storage_count}\n"

    boot_matches="$(collect_matches "${combined_regex}" "${boot_file}")"
    if [ -n "${boot_matches}" ]; then
      notable_matches="${notable_matches}# boot ${boot_label} (${boot_idx})\n"
      notable_matches="${notable_matches}$(printf '%s\n' "${boot_matches}" | sed "s|${COMMANDS_DIR}/||g" | sed -n '1,4p')\n"
    fi
  done

  {
    echo "## Recent Reboot Triage"
    echo
    echo "- Boots analyzed: ${analyzed_boots}"
    echo "- Scope: current boot plus the previous two boots when captured"
    echo

    if [ -n "${summary_rows}" ]; then
      printf '%s\n' '```text'
      printf '%b' "${summary_rows}"
      printf '%s\n' '```'
      echo
    fi

    if [ -n "${notable_matches}" ]; then
      echo "Notable matches:"
      echo
      printf '%s\n' '```text'
      printf '%b' "${notable_matches}"
      printf '%s\n' '```'
      echo
    fi
  } >>"${SUMMARY_FILE}"
}

render_xorg_comparison_readiness() {
  local session_file="${COMMANDS_DIR}/display-session-files.txt"
  local pkg_file="${COMMANDS_DIR}/rpm-display-session-packages.txt"
  local gdm_conf_file="${COMMANDS_DIR}/gdm-custom-conf.txt"
  local xsession_count
  local wayland_session_count
  local xorg_server_state
  local gnome_xsession_state
  local gdm_wayland_disabled="no"
  local comparison_ready="no"

  xsession_count="$(count_session_entries "${session_file}" "# /usr/share/xsessions")"
  wayland_session_count="$(count_session_entries "${session_file}" "# /usr/share/wayland-sessions")"
  xorg_server_state="$(rpm_query_pkg_state "${pkg_file}" "xorg-x11-server-Xorg")"
  gnome_xsession_state="$(rpm_query_pkg_state "${pkg_file}" "gnome-session-xsession")"

  if [ -f "${gdm_conf_file}" ] && grep -Eq '^[[:space:]]*WaylandEnable=false([[:space:]]*|$)' "${gdm_conf_file}" 2>/dev/null; then
    gdm_wayland_disabled="yes"
  fi

  if [ "${xsession_count}" -gt 0 ] && [ "${xorg_server_state}" = "installed" ]; then
    comparison_ready="yes"
  fi

  {
    echo "## Xorg Comparison Readiness"
    echo
    echo "- Xorg session entries detected: ${xsession_count}"
    echo "- Wayland session entries detected: ${wayland_session_count}"
    echo "- Xorg server package state: ${xorg_server_state}"
    echo "- GNOME Xorg session package state: ${gnome_xsession_state}"
    echo "- GDM login screen forced off Wayland: ${gdm_wayland_disabled}"
    echo "- Xorg comparison ready on this host: ${comparison_ready}"
    echo

    if [ "${comparison_ready}" = "yes" ]; then
      echo "Likely cluster:"
      echo "- A GDM session-menu comparison between Wayland and Xorg should be possible on this host."
      echo
    elif [ -f "${session_file}" ] || [ -f "${pkg_file}" ] || [ -f "${gdm_conf_file}" ]; then
      echo "Likely cluster:"
      echo "- This host does not currently expose an Xorg login option for comparison."
      if [ "${xorg_server_state}" = "missing" ]; then
        echo "- The Xorg server package is not installed."
      fi
      if [ "${xsession_count}" -eq 0 ]; then
        echo "- No Xorg desktop entries were captured under /usr/share/xsessions."
      fi
      echo
      printf '%s\n' '```text'
      if [ -f "${session_file}" ]; then
        sed "s|${COMMANDS_DIR}/||g" "${session_file}" | sed -n '1,20p'
      fi
      if [ -f "${pkg_file}" ]; then
        sed "s|${COMMANDS_DIR}/||g" "${pkg_file}" | sed -n '1,10p'
      fi
      if [ -f "${gdm_conf_file}" ]; then
        sed "s|${COMMANDS_DIR}/||g" "${gdm_conf_file}" | sed -n '1,20p'
      fi
      printf '%s\n' '```'
      echo
    fi
  } >>"${SUMMARY_FILE}"
}

render_historical_coredump_trends() {
  local coredump_file="${COMMANDS_DIR}/coredump-list.txt"
  local top_rows=""
  local codium_count=0
  local gnome_shell_count=0
  local xwayland_count=0
  local firefox_count=0
  local spotify_count=0
  local gcc_cc1_count=0
  local recurring_focus_count=0

  if [ -f "${coredump_file}" ]; then
    top_rows="$(
      awk '
        NF >= 10 && $1 != "TIME" && $1 !~ /^#/ {
          exe = $10
          count[exe]++
          latest[exe] = $1 " " $2 " " $3 " " $4
        }
        END {
          for (exe in count) {
            printf "%4d | %s | %s\n", count[exe], latest[exe], exe
          }
        }
      ' "${coredump_file}" 2>/dev/null | sort -nr | sed -n '1,10p'
    )"

    codium_count="$(grep -cF '/usr/share/codium/codium' "${coredump_file}" 2>/dev/null || true)"
    gnome_shell_count="$(grep -cF '/usr/bin/gnome-shell' "${coredump_file}" 2>/dev/null || true)"
    xwayland_count="$(grep -cF '/usr/bin/Xwayland' "${coredump_file}" 2>/dev/null || true)"
    firefox_count="$(grep -cF '/usr/lib64/firefox/firefox' "${coredump_file}" 2>/dev/null || true)"
    spotify_count="$(grep -Ec '/snap/spotify/.*/spotify' "${coredump_file}" 2>/dev/null || true)"
    gcc_cc1_count="$(grep -Ec '/usr/libexec/gcc/.*/cc1' "${coredump_file}" 2>/dev/null || true)"

    for count in "${codium_count}" "${gnome_shell_count}" "${xwayland_count}" "${firefox_count}" "${spotify_count}" "${gcc_cc1_count}"; do
      if [ "${count}" -gt 0 ]; then
        recurring_focus_count=$((recurring_focus_count + 1))
      fi
    done
  fi

  {
    echo "## Historical Coredump Trends"
    echo
    echo "- Coredump history captured: $([ -f "${coredump_file}" ] && echo yes || echo no)"
    echo "- Codium coredumps recorded: ${codium_count}"
    echo "- GNOME Shell coredumps recorded: ${gnome_shell_count}"
    echo "- Xwayland coredumps recorded: ${xwayland_count}"
    echo "- Firefox coredumps recorded: ${firefox_count}"
    echo "- Spotify coredumps recorded: ${spotify_count}"
    echo "- GCC cc1 coredumps recorded: ${gcc_cc1_count}"
    echo

    if [ -n "${top_rows}" ]; then
      echo "Likely cluster:"
      if [ "${codium_count}" -gt 0 ] && [ "${gnome_shell_count}" -gt 0 ]; then
        echo "- Codium is recurrent, but the compositor also has an independent crash history."
      fi
      if [ "${recurring_focus_count}" -ge 3 ]; then
        echo "- Multiple unrelated GUI apps have historical coredumps, which points to broader session/display/platform instability rather than a single bad app profile."
      fi
      echo
      printf '%s\n' '```text'
      printf '%s\n' "${top_rows}"
      printf '%s\n' '```'
      echo
    fi
  } >>"${SUMMARY_FILE}"
}


gpu_pcie_link_rows() {
  local file="${COMMANDS_DIR}/gpu-pcie-links.txt"
  [ -f "${file}" ] || return 0

  awk '
    function flush() {
      if (device == "") {
        return
      }
      current_width_num = current_width + 0
      max_width_num = max_width + 0
      degraded = "no"
      if (current_width_num > 0 && max_width_num > 0 && current_width_num < max_width_num) {
        degraded = "yes"
      }
      speed_downshift = "no"
      if (current_speed != "" && max_speed != "" && current_speed != max_speed) {
        speed_downshift = "yes"
      }
      printf "%s\t%s\t%s\t%s\t%s\t%s\t%s\t%s\t%s\n", device, driver, current_width, max_width, current_speed, max_speed, degraded, speed_downshift, subsystem_vendor ":" subsystem_device
    }
    /^pci_device=/ { flush(); device = substr($0, 12); driver = ""; current_width = ""; max_width = ""; current_speed = ""; max_speed = ""; subsystem_vendor = ""; subsystem_device = ""; next }
    /^driver=/ { driver = substr($0, 8); next }
    /^current_link_width=/ { current_width = substr($0, 20); next }
    /^max_link_width=/ { max_width = substr($0, 16); next }
    /^current_link_speed=/ { current_speed = substr($0, 20); next }
    /^max_link_speed=/ { max_speed = substr($0, 16); next }
    /^subsystem_vendor=/ { subsystem_vendor = substr($0, 18); next }
    /^subsystem_device=/ { subsystem_device = substr($0, 18); next }
    END { flush() }
  ' "${file}"
}

