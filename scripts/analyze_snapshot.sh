  gpu_bridge="$(grep -E '(^|[^0-9])1b\.4-\[03\].*NVIDIA|00:1b\.4-\[03\].*NVIDIA|(^|[^0-9])01\.0-\[01\].*NVIDIA|00:01\.0-\[01\].*NVIDIA|03:00\.0.*NVIDIA' "${lspci_tree_file}" 2>/dev/null | head -n 1 || true)"
