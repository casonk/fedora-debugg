  elif grep -Eiq '(^|[^0-9])01\.0-\[[^]]+\].*(NVIDIA|VGA|Display)|00:01\.0-\[[^]]+\].*(NVIDIA|VGA|Display)' "${lspci_tree_file}" 2>/dev/null; then
    cpu_peg_empty="no"
